function loadRecipes(list){
  allRecipes.innerHTML="";
  list.forEach(r=>{
    allRecipes.innerHTML+=`
      <div class="card">
        <img src="${r.img}" onclick='openRecipe(${JSON.stringify(r)})'>
        <h3>${r.name}</h3>
      </div>`;
  });
}

function openRecipe(r){
  detailImg.src=r.img;
  detailName.innerText=r.name;
  detailTime.innerText=r.time;
  detailIngredients.innerText=r.ingredients;
  detailMethod.innerHTML="";
  r.method.forEach(step=>{
    const li=document.createElement("li");
    li.innerText=step;
    detailMethod.appendChild(li);
  });
  showPage("recipeDetail");
}

function performSearch(){
  const q = searchInput.value.toLowerCase().trim();

  const filtered = recipes.filter(r => {
    const nameMatch = r.name.toLowerCase().includes(q);

    // Convert ingredients array to string
    const ingredientsText = Array.isArray(r.ingredients)
      ? r.ingredients.join(" ").toLowerCase()
      : r.ingredients.toLowerCase();

    const ingredientMatch = ingredientsText.includes(q);

    return nameMatch || ingredientMatch;
  });

  showPage("recipes");

  if(filtered.length === 0){
    allRecipes.innerHTML = "";
    noResult.style.display = "block";
  } else {
    noResult.style.display = "none";
    loadRecipes(filtered);
  }
}


